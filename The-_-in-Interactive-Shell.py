# You can obtain the result of the last expression with the underscore operator,
# e.g. in IPython this looks like:

In[1]: 3 * 3
Out[1]: 9

In[2]: _ + 3
Out[2]: 12

# This works in the Python shell too. In addition, the IPython shell allows you
# to use Out[n] to get the value of the expression In[n]. E.g., Out[1] would
# give us the number 9 in the example above.
